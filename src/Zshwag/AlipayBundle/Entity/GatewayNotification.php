<?php

namespace Zshwag\AlipayBundle\Entity;

/**
 * GatewayNotification
 */
class GatewayNotification
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $content;

    /**
     * @var int
     */
    private $receivedAt;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return GatewayNotification
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * Set receivedAt
     *
     * @param integer $receivedAt
     *
     * @return GatewayNotification
     */
    public function setReceivedAt($receivedAt)
    {
        $this->receivedAt = $receivedAt;

        return $this;
    }

    /**
     * Get receivedAt
     *
     * @return int
     */
    public function getReceivedAt()
    {
        return $this->receivedAt;
    }
}

